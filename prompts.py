def build_context_prompt(knowledge_list: list[dict], ad_data_summary: str) -> str:
    """Строит контекстный блок из знаний и данных для передачи в GigaChat."""
    parts = []

    if knowledge_list:
        parts.append("=== БАЗА ЗНАНИЙ (справки по рекламным платформам) ===")
        for k in knowledge_list:
            ch = f"[{k['channel']}] " if k.get("channel") else ""
            parts.append(f"Источник: {ch}{k['source']}\n{k['content'][:3000]}")  # лимит на справку
        parts.append("=== КОНЕЦ БАЗЫ ЗНАНИЙ ===\n")

    if ad_data_summary and ad_data_summary != "Рекламные данные не загружены.":
        parts.append("=== ЗАГРУЖЕННЫЕ РЕКЛАМНЫЕ ДАННЫЕ ===")
        parts.append(ad_data_summary[:8000])  # лимит на данные
        parts.append("=== КОНЕЦ ДАННЫХ ===\n")

    return "\n\n".join(parts)
